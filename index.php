         <p>A very wealthy man told me one time, "People will always need to eat and sleep somewhere".
         Well needless to say he was very correct in his assumption. He himself was in real estate. He built homes and apartment houses and never ran out of buyers and renters.<p>

         <p>This site will look at the eating aspect. Going out to eat is a ritual everybody indulges in nearly everyday. It is the way our culture socializes. We go out to eat for entertainment, business, family get togethers, breakfast, lunch, dinner, just to try a new place, boredom, and any other reason we can think of.</p>

         <p>Then, where we go to eat means so much to some people. We have our favorite places, our out of the way places, our special places. It takes time to find these places most close to our hearts. Many people are very serious about their choice for a favorite dinery. Whether it's the food, the wait staff, the owners, location, it all comes together to give us the grand experience of a lifetime.</P>

         <P>This site will share with you some of our experiences at some local eateries. Our likes, dislikes, winners and losers. You may know some of our choices, you may not. It is our hope to help guide you to a place where you too might want to call it, "Your new favorite".</p>
         
